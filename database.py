def list_collections():
    conn = get_connection()
    rows = conn.execute("""
        SELECT c.id, c.name, COUNT(cv.video_id) as video_count
        FROM collections c
        LEFT JOIN collection_videos cv ON c.id = cv.collection_id
        GROUP BY c.id
        ORDER BY c.name
    """).fetchall()
    conn.close()
    return [dict(r) for r in rows]


