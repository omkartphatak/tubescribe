# --- Tags ---

def save_tags(video_id, tags):
    conn = get_connection()
    for tag in tags:
        tag = tag.strip().lower()
        if tag:
            conn.execute(
                "INSERT OR IGNORE INTO video_tags (video_id, tag) VALUES (?, ?)",
                (video_id, tag),
            )
    conn.commit()
    conn.close()


def get_tags(video_id):
    conn = get_connection()
    rows = conn.execute(
        "SELECT tag FROM video_tags WHERE video_id = ? ORDER BY tag", (video_id,)
    ).fetchall()
    conn.close()
    return [r["tag"] for r in rows]


def get_all_tags():
    conn = get_connection()
    rows = conn.execute("""
        SELECT tag, COUNT(*) as count
        FROM video_tags
        GROUP BY tag
        ORDER BY count DESC, tag
    """).fetchall()
    conn.close()
    return [dict(r) for r in rows]


def get_videos_by_tag(tag):
    conn = get_connection()
    rows = conn.execute("""
        SELECT v.id, v.video_id, v.url, v.title, v.channel, v.duration, v.detail_level, v.processed_at
        FROM videos v
        JOIN video_tags vt ON v.video_id = vt.video_id
        WHERE vt.tag = ?
        ORDER BY v.processed_at DESC
    """, (tag,)).fetchall()
    conn.close()
    return [dict(r) for r in rows]


# --- Collections ---

def create_collection(name):
    conn = get_connection()
    cursor = conn.execute("INSERT OR IGNORE INTO collections (name) VALUES (?)", (name.strip(),))
    conn.commit()
    cid = cursor.lastrowid
    if not cid:
        row = conn.execute("SELECT id FROM collections WHERE name = ?", (name.strip(),)).fetchone()
        cid = row["id"] if row else None
    conn.close()
    return cid


def delete_collection(collection_id):
    conn = get_connection()
    conn.execute("DELETE FROM collection_videos WHERE collection_id = ?", (collection_id,))
    conn.execute("DELETE FROM collections WHERE id = ?", (collection_id,))
    conn.commit()
    conn.close()


